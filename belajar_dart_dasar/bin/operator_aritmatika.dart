void main(List<String> args) {
  /**
   * Operator aritmatika:
   * + -> Penjumlahan
   * - -> pengurangan atau negatif
   * * -> Perkalian
   * / -> Pembagian, hasil Double
   * ~/ -> pembagian, hasil int
   * % -> Sisa Bagi
   */

  var number1 = 10;
  var number2 = 3;

  print(number1 + number2);
  print(number1 - number2);
  print(number1 * number2);
  print(number1 / number2);
  print(number1 ~/ number2);
  print(number1 % number2);
  print(5 * (3 + -3));
}
