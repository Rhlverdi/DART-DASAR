void main(List<String> args) {
  /**
   * operator perbandingan -> operator yang hasilny boolean
   * bisa digunakan di tipe data number dan string
   * == --> sama dengan
   * != --> tidak sama dengan
   * > --> lebih dari
   * < --> kurang dari
   * >= --> lebih dari sama dengan
   * <= --> kurang dari sama dengan 
  */

  print(11 == 11);
  print(11 != 12);
  print(11 > 10);
  print(10 < 11);
  print(12 >= 11);
  print(10 <= 11);

  print("a" == "a");
  print("yusman" == "YUSMAN");

  ///print("true" == true);
  ///print("11" == 11);
}
